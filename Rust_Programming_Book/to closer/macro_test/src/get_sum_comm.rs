macro_rules! get_sum {
    // [규칙 1] 매크로 패턴(pattern) 정의 영역
    // ($($x:expr), *) :
    //  - $x      : 매크로 변수 이름
    //  - :expr   : '식(expression)'만 받겠다는 타입 제약
    //  - $(...),*: 0개 이상 반복 가능, 쉼표로 구분됨
    ($($x:expr), *) => {{

        // [규칙 2] 매크로는 "코드 조각"을 그대로 생성한다
        // 이 블록 전체가 호출 위치에 치환됨
        let args = vec![$($x), *];
        //           └── [규칙 3] 패턴에서 쓴 반복 구조는
        //               결과 코드에서도 동일하게 반복해야 함

        // [규칙 4] 매크로 내부 코드는 일반 Rust 코드와 동일
        args.iter().sum::<i32>()
    }};
}
